    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed
        if(jTextFieldCodigo.getText().equals("")){
            FrmConsultaRevista frmContulta = new FrmConsultaRevista(null, true);
            frmContulta.setVisible(true);          
            
            String idRevista = frmContulta.getIdCodigo();
            String numEdicao = frmContulta.getNumeroEdicao();
            String ano = frmContulta.getAnoRevista();
            String caixa = frmContulta.getCaixaRevista();
            String colecao = frmContulta.getColecao();
            
            jTextFieldCodigo.setText(idRevista);
            jTextFieldNumeroEdicao.setText(numEdicao);
            jTextFieldAno.setText(ano);  
            jTextFieldCaixa.setSelectedItem(caixa);
            jTextFieldColecao.setText(colecao);
            
            jMenuNovo.setEnabled(true);
            jMenuSalvar.setEnabled(false);
            jMenuEditar.setEnabled(true);
            jMenuExluir.setEnabled(true);
            jMenuCancelar.setEnabled(true);
            jButtonBuscar.setEnabled(false);
        }else{
            
            control.CarregarRevistas(Integer.parseInt(jTextFieldCodigo.getText().toString()));          
            int numEdicao = control.getNumeroRev(); 
            int anoEdicao = control.getAnoRev();
            String caixaRevi = control.getCaixaRev();
            String colecao = control.getColecao();
            jTextFieldNumeroEdicao.setText(String.valueOf(numEdicao));
            jTextFieldAno.setText(String.valueOf(anoEdicao));  
            jTextFieldCaixa.setSelectedItem(String.valueOf(caixaRevi));
            jTextFieldColecao.setText(colecao);
            
            jMenuNovo.setEnabled(true);
            jMenuSalvar.setEnabled(false);
            jMenuEditar.setEnabled(true);
            jMenuExluir.setEnabled(true);
            jMenuCancelar.setEnabled(true);
            jButtonBuscar.setEnabled(false);
            
        }
    }//GEN-LAST:event_jButtonBuscarActionPerformed

