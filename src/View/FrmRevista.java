    public void buscarUltimaInsercao(){
        String numEdicao = jTextFieldNumeroEdicao.getText().toString();
        //conecta.desconecta();
        conecta.conexao(); 
        conecta.executaSQL("SELECT max(id_tblRevistas) as numeroPego FROM tblRevistas where numeroEdicao_tblRevistas = '"+numEdicao+"'");
        try { 
            while(conecta.rs.next()){
                String idPego = conecta.rs.getString("numeroPego");
                System.out.println(idPego);
                jTextFieldCodigo.setText(idPego);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao buscar id da coleção!\nERRO:"+ex);
        }
    }
    
    public void preencherComboboxCaixa(){
        
        conecta.conexao();
        conecta.executaSQL("select * from tblCaixa order by numero_tblCaixa");
               try{
            conecta.rs.next();
            jTextFieldCaixa.removeAllItems();
            
            do{
                jTextFieldCaixa.addItem(conecta.rs.getString("numero_tblCaixa"));
                
                
            }while(conecta.rs.next());
        }catch (SQLException ex){
            JOptionPane.showMessageDialog(rootPane, "Erro ao preencher combobox!\nERRO:" + ex);
        }
    }
    
    
    
    

