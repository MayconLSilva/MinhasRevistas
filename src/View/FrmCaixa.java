    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed
        if(jTextFieldCodigo.getText().equals("")){
            FrmConsultaCaixa frmContulta = new FrmConsultaCaixa(null, true);
            frmContulta.setVisible(true);          
            
            String idColecao = frmContulta.getIdCodigo();
            String numero = frmContulta.getNumero();
            String codEtiqueta = frmContulta.getCodEtiqueta();
            String cor = frmContulta.getCor();
            
            jTextFieldCodigo.setText(idColecao);
            jTextFieldNumeroCaixa.setText(numero);
            jTextFieldCodEtiqueta.setText(codEtiqueta);  
            jTextFieldCor.setText(cor);
            
            jMenuNovo.setEnabled(true);
            jMenuSalvar.setEnabled(false);
            jMenuEditar.setEnabled(true);
            jMenuExluir.setEnabled(true);
            jMenuCancelar.setEnabled(true);
            jButtonBuscar.setEnabled(false);
        }else{
            
            control.CarregarCaixa(Integer.parseInt(jTextFieldCodigo.getText().toString()));          
            int numero = control.getNum(); 
            String idenf = control.getIdentif();
            String cor = control.getCor();
            jTextFieldNumeroCaixa.setText(String.valueOf(numero));
            jTextFieldCodEtiqueta.setText(idenf);  
            jTextFieldCor.setText(cor);  
            
            jMenuNovo.setEnabled(true);
            jMenuSalvar.setEnabled(false);
            jMenuEditar.setEnabled(true);
            jMenuExluir.setEnabled(true);
            jMenuCancelar.setEnabled(true);
            jButtonBuscar.setEnabled(false);
            
        }
    }//GEN-LAST:event_jButtonBuscarActionPerformed

    public void buscarUltimaInsercao(){
        String nomeAmigo = jTextFieldNumeroCaixa.getText().toString();
        //conecta.desconecta();
        conecta.conexao(); 
        conecta.executaSQL("SELECT max(id_tblAmigo) as numeroPego FROM tblAmigo where nome_tblAmigo = '"+nomeAmigo+"'");
        try { 
            while(conecta.rs.next()){
                String idPego = conecta.rs.getString("numeroPego");
                System.out.println(idPego);
                jTextFieldCodigo.setText(idPego);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao buscar id da coleção!\nERRO:"+ex);
        }
    }
    
    
    
    

