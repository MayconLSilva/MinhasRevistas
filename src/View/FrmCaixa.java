    private void jMenuSalvarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jMenuSalvarMouseClicked
        if(jMenuSalvar.isEnabled()){
            //IF SALVAR
            if(flag == 1){
                mod.setNumeroCaixa(Integer.parseInt(jTextFieldNumeroCaixa.getText()));
                mod.setIdentificacaoEtiquetaCaixa(jTextFieldCodEtiqueta.getText());
                mod.setCorCaixa(jTextFieldCor.getText());
                control.SalvarCaixa(mod);
                
                buscarUltimaInsercao();
                
                //Método ativa os menus
                jMenuNovo.setEnabled(true);
                jMenuSalvar.setEnabled(false);
                jMenuEditar.setEnabled(true);
                jMenuExluir.setEnabled(true);
                jMenuCancelar.setEnabled(false);
                
                jTextFieldCodigo.setEnabled(true);
                jTextFieldNumeroCaixa.setEnabled(false);
                jTextFieldCodEtiqueta.setEnabled(false);  
                jTextFieldCor.setEnabled(false);          
            }
            //ELSE EDITAR
            else{
                mod.setNumeroCaixa(Integer.parseInt(jTextFieldNumeroCaixa.getText()));
                mod.setIdentificacaoEtiquetaCaixa(jTextFieldCodEtiqueta.getText());
                mod.setCorCaixa(jTextFieldCor.getText());
                mod.setIdCaixa(Integer.parseInt(jTextFieldCodigo.getText()));
                control.EditarCaixa(mod);
                
                //buscarUltimaInsercao();
                
                //Método ativa os menus
                jMenuNovo.setEnabled(true);
                jMenuSalvar.setEnabled(false);
                jMenuEditar.setEnabled(true);
                jMenuExluir.setEnabled(true);
                jMenuCancelar.setEnabled(false);
                
                jTextFieldCodigo.setEnabled(true);
                jTextFieldNumeroCaixa.setEnabled(false); 
                jTextFieldCodEtiqueta.setEnabled(false);  
                jTextFieldCor.setEnabled(false);          
            }            
        }                
        
    }//GEN-LAST:event_jMenuSalvarMouseClicked

